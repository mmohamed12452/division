package Ordering.Cat;


public class Barry {
   String firstName = "Henry";
   String lastName = "Tiller";
   String email = "TillerHenry@gmail.com";
   int age = 18;

    public static void main(String[] args) {
        Barry mybarry = new Barry();
        System.out.println("First name: " + mybarry.firstName);
        System.out.println("Last name: " + mybarry.lastName);
        System.out.println("Email: " + mybarry.email);
        System.out.println("Age:" + mybarry.age);
    }
}
